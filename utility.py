import values

# Method to get width by percentage
def width_prct(percentage):
    return (percentage/100)*values.WIDTH

# Method to get height by percentage
def height_prct(percentage):
    return (percentage/100)*values.HEIGHT

# Adding width
def width_addition(add):
    return values.WIDTH + add
# Adding height
def height_addition(add):
    return values.HEIGHT + add

    